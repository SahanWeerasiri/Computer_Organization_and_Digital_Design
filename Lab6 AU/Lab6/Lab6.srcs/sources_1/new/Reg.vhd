entity Reg is
    Port ( D : in STD_LOGIC_VECTOR (3 downto 0);
           En : in STD_LOGIC;
           Clk : in STD_LOGIC;
           Q : out STD_LOGIC_VECTOR (3 downto 0));
end Reg;

architecture Behavioral of Reg is

begin
    PROCESS (Clk)
        BEGIN
            IF (RISING_EDGE(Clk)) THEN
                IF (EN='1') THEN
                    Q<=D;
                END IF;
            END IF;
    END PROCESS;


end Behavioral;
